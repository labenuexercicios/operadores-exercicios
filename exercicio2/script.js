const primeiroNumero = Number(prompt("Insira um numero")) // 100
const segundoNumero = Number(prompt("Insira um segundo numero")) // 50

console.log(`O primeiro numero é maior que segundo? ${primeiroNumero > segundoNumero}`) // true
console.log(`O primeiro numero é igual ao segundo? ${primeiroNumero === segundoNumero}`) // false 
console.log(`O primeiro numero é divisivel pelo segunda? ${primeiroNumero % segundoNumero=== 0}`) // true
console.log(`O segundo numero é divisivel pelo primeiro? ${segundoNumero % primeiroNumero=== 0}`) // false


